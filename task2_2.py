num1 = int(input("Enter the first number : "))
num2 = int(input("Enter the second number : "))

print("The sum of {0} and {1} is {2}".format(num1, num2, num1+num2))
print("The substraction result of {0} and {1} is {2}".format(
    num1, num2, num1-num2))
print("The multiplication result of {0} and {1} is {2}".format(
    num1, num2, num1*num2))
print("The division result of {0} and {1} is {2}".format(
    num1, num2, num1/num2))
